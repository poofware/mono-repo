/// Generates a date string "YYYY-MM-DD" for today + [dayOffset].
String _makeServiceDate(int dayOffset) {
  final dt = DateTime.now().add(Duration(days: dayOffset));
  return DateFormat('yyyy-MM-dd').format(dt);
}

